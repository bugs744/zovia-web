#!/usr/bin/env node
// Build-time helper: compiles views/*.ejs to standalone render functions and
// emits functions/_templates.js. Cloudflare Pages Functions (V8 isolates)
// can't use ejs at runtime because ejs imports node:fs; precompiling to
// pure-JS closures sidesteps that entirely.
//
// EJS's client-compile mode emits bare variable references like `title`
// rather than `locals.title` — when ES modules forbid `with (locals)`,
// those references break. `destructuredLocals` fixes it by prepending a
// destructure like `var { title, message } = locals`.
//
// Runs via `npm run build` (invoked by Cloudflare Pages during deploy).

const fs = require('fs');
const path = require('path');
const ejs = require('ejs');

const VIEWS_DIR = path.join(__dirname, '..', 'views');
const OUT_FILE = path.join(__dirname, '..', 'functions', '_templates.js');

// Variables each template reads from the `locals` argument. Keep this list
// in sync with the EJS files — new <%= var %> interpolations must be added.
const TEMPLATE_LOCALS = {
  'error': ['title', 'message'],
  'join-space': [
    'spaceName',
    'spaceDescription',
    'memberCount',
    'ownerName',
    'inviteToken',
    'spaceType',
    'appId',
    'appDisplayName',
    'appConfig',
  ],
};

const files = fs.readdirSync(VIEWS_DIR).filter((f) => f.endsWith('.ejs'));

const modules = files.map((file) => {
  const name = path.basename(file, '.ejs');
  const exportName = name.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
  const destructuredLocals = TEMPLATE_LOCALS[name];
  if (!destructuredLocals) {
    throw new Error(
      `Missing TEMPLATE_LOCALS entry for "${name}.ejs". ` +
      `Add it to scripts/build-templates.js.`,
    );
  }

  const raw = fs.readFileSync(path.join(VIEWS_DIR, file), 'utf8');
  const fnSource = ejs
    .compile(raw, {
      client: true,
      strict: true,
      destructuredLocals,
    })
    .toString();
  return `export const render${exportName[0].toUpperCase() + exportName.slice(1)} = ${fnSource};`;
});

const header = `// AUTO-GENERATED by scripts/build-templates.js — DO NOT EDIT.
// Run \`npm run build\` to regenerate.
//
// Pure-JS render functions compiled from views/*.ejs via ejs.compile({
// client: true, strict: true, destructuredLocals }). Callers invoke as
//   renderFoo(locals, escapeFn)
// where escapeFn HTML-escapes a string.

`;

fs.writeFileSync(OUT_FILE, header + modules.join('\n\n') + '\n');
console.log(`Compiled ${modules.length} templates → ${path.relative(process.cwd(), OUT_FILE)}`);
